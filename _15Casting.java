public class _15Casting {
    public static void main(String[] args) {
        // 32 bit
        int a = 25;
        // 16 bit
        short b = 25;
        // 8 bit
        byte c = 25;
        // 64 bit
        long d = 25; 

        // 4 bytes
        float e = 25.5f;

        // 8 bytes
        double f = 923.5;

        // To cast int value
        // Only if casted value can be fit
        a = (int) d;
    }
}
