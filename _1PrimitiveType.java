public class _1PrimitiveType {
    public static void main(String[] args) {
        System.out.println("Hello Java");

        // Primitive Data Types

        // 1) Integer variable (32 bit)
        int a;
        a = 42;
        System.out.println(a);

        // 2) Short integer variable (16 bit)
        short b = 42;
        System.out.println(b);

        // 3) long integer variable
        long c = 123456;
        System.out.println(c);

        // 4) double (integer with fraction) variable
        double d = 42.52345;
        System.out.println(d);

        // 5) Float (short version of double) variable
        // Float must taken f at the end of the value.
        float e = 123.1213f;
        System.out.println(e);

        // 6) Char (hold unicode character)
        char f = 'f';
        System.out.println(f);

        // 7) Boolean
        boolean g = true;
        System.out.println(g);

        // 8) Byte type (only hold 6bit data) (-128 to 127)
        byte h = 123;
        System.out.println(h);

    }
}